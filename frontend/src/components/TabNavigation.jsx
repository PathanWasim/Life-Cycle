const TabNavigation = ({ tabs = [], activeTab, onTabChange }) => {
  return (
    <div style={{
      background: 'var(--surface)',
      border: '1px solid var(--border)',
      borderRadius: 'var(--radius-xl)',
      padding: '0.375rem',
      marginBottom: '1.5rem',
      display: 'flex',
      gap: '0.25rem',
      overflowX: 'auto',
      boxShadow: 'var(--shadow-sm)',
      scrollbarWidth: 'none',
    }}>
      {tabs.map(tab => {
        const isActive = activeTab === tab.id;
        const icon = ICONS[tab.id] || ICONS[tab.icon] || null;
        return (
          <button
            key={tab.id}
            onClick={() => onTabChange(tab.id)}
            style={{
              display: 'flex',
              alignItems: 'center',
              gap: '0.5rem',
              padding: '0.5rem 1.1rem',
              borderRadius: 'var(--radius-lg)',
              border: 'none',
              cursor: 'pointer',
              fontFamily: 'var(--font-sans)',
              fontSize: '0.815rem',
              fontWeight: 600,
              whiteSpace: 'nowrap',
              transition: 'all var(--transition-base)',
              letterSpacing: '0.01em',
              position: 'relative',
              background: isActive
                ? 'linear-gradient(135deg, var(--primary) 0%, var(--primary-dark) 100%)'
                : 'transparent',
              color: isActive ? 'white' : 'var(--text-secondary)',
              boxShadow: isActive
                ? '0 4px 12px rgba(220, 38, 38, 0.3)'
                : 'none',
            }}
            onMouseEnter={e => {
              if (!isActive) {
                e.currentTarget.style.background = 'var(--bg-tertiary)';
                e.currentTarget.style.color = 'var(--text-primary)';
                e.currentTarget.style.borderBottom = '2px solid var(--primary)';
              }
            }}
            onMouseLeave={e => {
              if (!isActive) {
                e.currentTarget.style.background = 'transparent';
                e.currentTarget.style.color = 'var(--text-secondary)';
                e.currentTarget.style.borderBottom = 'none';
              }
            }}
          >
            {icon && (
              <span style={{ display: 'flex', alignItems: 'center', opacity: isActive ? 1 : 0.7 }}>
                {icon}
              </span>
            )}
            {tab.label}
            {tab.badge !== undefined && (
              <span style={{
                background: isActive ? 'rgba(255,255,255,0.25)' : 'var(--primary)',
                color: 'white',
                fontSize: '0.62rem',
                fontWeight: 700,
                padding: '1px 6px',
                borderRadius: 'var(--radius-full)',
                minWidth: 18,
                textAlign: 'center',
                lineHeight: 1.5,
              }}>
                {tab.badge}
              </span>
            )}
          </button>
        );
      })}
    </div>
  );
};

export default TabNavigation;